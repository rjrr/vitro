package vitro.virtualsensor.utils;

public class Phenomena {
    public String id = "";
    public String urn = "";
    public String lightCode = "";
    public String defaultUnitId = "";
    
    @Override public String toString () {
        String res = "Phenomena: " + id;
        res += "\n\tURN         : " + urn;
        res += "\n\tLight Code  : " + lightCode;
        res += "\n\tDefault Unit: " + defaultUnitId;
        return res;
    }
}